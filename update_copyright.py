def replace_text_in_file(file: Path):
    if file == Path(__file__):
        return
    with file.open('r') as f:
        text = f.read()
    if text_to_find in text:
        print(f'Replacing in', file)
        text = text.replace(text_to_find, text_to_replace)
        with file.open('w') as f:
            f.write(text)

